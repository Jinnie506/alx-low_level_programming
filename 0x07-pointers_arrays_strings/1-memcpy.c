#include "holberton.h"

/**
 *_memcpy - function that copies memory area
 *@dest: function parameter
 *@src: function parameter
 *@n: function parameter
 *Return: pointer
 */

char *_memcpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
	{
		dest[i] = src[i];
	}
	return (dest);
}
